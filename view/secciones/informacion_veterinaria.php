    <h5>¿Todas las mascotas deben ir (perros, gatos, aves, peces, roedores…)?  </h5>
	<p>Deben ir los perros y gatos. Las otras mascotas no necesitan visitas preventivas si no están con síntomas de enfermedad. Es preferible que no salgan de su hábitat, ya que se estresan mucho.
