    <h5>¿Cada cuánto hay que ir al veterinario?</h5>
    <p>Las visitas al médico dependen de la edad del animal y su estado de salud. Te damos algunas pautas para saber cuándo acercarte, ya sea para un chequeo de rutina o en casos de emergencias.
	Las visitas al veterinario son fundamentales para la salud de las mascotas. Deben ser regulares, por más que no se detecte ningún signo o síntoma que te haga sospechar de una enfermedad del animal. Esto se debe a que la medicina veterinaria tiende a ser preventiva y apunta a la detección precoz de posibles patologías en sus primeras manifestaciones. </p>			
    </div>  
    <div class="col-12 col-sm-12 col-md-12 col-lg-4 col-xl-4 ">
    <h5>¿Con qué frecuencia hay que llevar a la mascota al veterinario? </h5>
    <p>Los cachorros deberán ir frecuentemente hasta terminar su plan sanitario. Este depende de cada profesional y de la zona en que habite. Los adultos tienen que ir por lo menos una vez al año, idealmente dos veces al año, siempre y cuando estén en buen estado de salud.  Recordar revacunar anualmente por lo menos con la séxtuple y rabia (esta última obligatoria).</p>                  
